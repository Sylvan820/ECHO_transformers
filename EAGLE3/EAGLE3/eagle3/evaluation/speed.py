import json
from transformers import AutoTokenizer
import numpy as np

# Ensure the model path is correct
tokenizer = AutoTokenizer.from_pretrained("/wings/zeus-kimi-k2-flush-a-na131-m-test/hub/models--Qwen--Qwen3-32B/snapshots/9216db5781bf21249d130ec9da846c4624c16137/")

jsonl_file_eagle = "qwen3_8b_eagle3-temperature-0.0.jsonl"
jsonl_file_base = "qwen3_8b_baseline-temperature-0.0.jsonl"

data_eagle = []
with open(jsonl_file_eagle, 'r', encoding='utf-8') as file:
    for line in file:
        json_obj = json.loads(line)
        data_eagle.append(json_obj)

speeds_eagle = []
all_mats = [] # List to store MAT (Mean Accepted Tokens) from all turns

for datapoint in data_eagle:
    # 1. Calculate Speed (tokens per second)
    tokens = sum(datapoint["choices"][0]['new_tokens'])
    times = sum(datapoint["choices"][0]['wall_time'])
    if times > 0:
        speeds_eagle.append(tokens / times)
    
    # 2. Collect Mean Accepted Tokens (MAT)
    # Note: This field 'mean_accepted_tokens' must exist in the jsonl generated by the updated gen_ea_answer_qwen3.py
    if 'mean_accepted_tokens' in datapoint["choices"][0]:
        mats = datapoint["choices"][0]['mean_accepted_tokens']
        all_mats.extend(mats) # Add MATs from each turn to the aggregate list

data_base = []
with open(jsonl_file_base, 'r', encoding='utf-8') as file:
    for line in file:
        json_obj = json.loads(line)
        data_base.append(json_obj)

speeds_base = []
for datapoint in data_base:
    answer = datapoint["choices"][0]['turns']
    tokens = 0
    # Recalculate token count using the tokenizer to ensure consistency
    for i in answer:
        # Subtracting 1 typically removes the BOS token or similar overhead; adjust if needed for your specific tokenizer
        tokens += (len(tokenizer(i).input_ids) - 1)
    
    times = sum(datapoint["choices"][0]['wall_time'])
    if times > 0:
        speeds_base.append(tokens / times)

mean_speed_eagle = np.array(speeds_eagle).mean()
mean_speed_base = np.array(speeds_base).mean()
mean_mat = np.array(all_mats).mean() if all_mats else 0.0

print(f"EAGLE3 Mean Speed: {mean_speed_eagle:.2f} tokens/s")
print(f"Baseline Mean Speed: {mean_speed_base:.2f} tokens/s")
print(f"Speedup Ratio: {mean_speed_eagle / mean_speed_base:.4f}")
print(f"Mean Accepted Tokens (MAT): {mean_mat:.4f}")